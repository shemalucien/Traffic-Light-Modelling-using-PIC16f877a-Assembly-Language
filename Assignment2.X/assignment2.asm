STATUS equ 03h
RP0 equ 05
RP1 equ 06
PORTA equ 05h
TRISA equ 85h
 
 
 COUNTER equ 20h 
 
 
    org 00 
    clrw 
    
    BCF STATUS, RP0 
    BCF STATUS, RP1 

    CLRF PORTA 
    
    BSF STATUS, RP0
    
    MOVLW 0x00 
    
    MOVWF TRISA 
    
    BCF STATUS,	RP0 
    BCF STATUS,	RP1 
 
START	MOVLW  B'00000010'
	MOVWF PORTA  
    
	MOVLW 0xff
	MOVWF COUNTER 

LOOP1	DECFSZ COUNTER
	GOTO LOOP1
	
	CLRF PORTA

	MOVLW B'00000100'
	MOVWF PORTA  
    
	MOVLW 0xff
	MOVWF COUNTER 
	
LOOP2	DECFSZ COUNTER
	GOTO LOOP2
	
	CLRF PORTA
	MOVLW B'00001000'
	MOVWF PORTA
	
	MOVLW 0xff
	MOVWF COUNTER	
	
LOOP3	DECFSZ COUNTER
        GOTO LOOP3
	
	GOTO START 
END 
	
